import './Landmark.css'
import { useRef } from "react";
import {
  motion,
  useScroll,
  useSpring,
  useTransform,
  useMotionValue,
  useVelocity,
  useAnimationFrame
} from "framer-motion";

function wrap(min: number, max: number, v: number) {
  const lo = Math.min(min, max);
  const hi = Math.max(min, max);
  const range = hi - lo;
  if (range === 0) return lo;

  const mod = ((v - lo) % range + range) % range;
  return lo + mod;
}

interface ParallaxProps {
  children: string;
  baseVelocity: number;
}

function ParallaxText({ children, baseVelocity = 100 }: ParallaxProps) {
  const baseX = useMotionValue(0);
  const { scrollY } = useScroll();
  const scrollVelocity = useVelocity(scrollY);
  const smoothVelocity = useSpring(scrollVelocity, {
    damping: 50,
    stiffness: 400
  });
  const velocityFactor = useTransform(smoothVelocity, [0, 4000], [0, 5], {
    clamp: false
  });

  const x = useTransform(baseX, (v) => `${wrap(-20, -45, v)}%`);

  const directionFactor = useRef<number>(1);
  useAnimationFrame((t, delta) => {
    let moveBy = directionFactor.current * baseVelocity * (delta / 1000);

    if (velocityFactor.get() < 0) {
      directionFactor.current = -1;
    } else if (velocityFactor.get() > 0) {
      directionFactor.current = 1;
    }

    moveBy += directionFactor.current * moveBy * velocityFactor.get();

    baseX.set(baseX.get() + moveBy);
  });

  return (
    <div className="parallax from-black/50 via-black/20 to-black/30 inset-0 bg-gradient-to-t">
      <motion.div className="scroller" style={{ x }}>
        <span>{children} </span>
        <span>{children} </span>
        <span>{children} </span>
        <span>{children} </span>
      </motion.div>
    </div>
  );
}

export default function Landmark() {
  return (
    <section>
      <ParallaxText baseVelocity={-5}>
        Architectural Excellence
      </ParallaxText>
      <ParallaxText baseVelocity={5}>
        Modern Spaces • Innovative Design • Timeless Architecture
      </ParallaxText>
    </section>
  );
}
